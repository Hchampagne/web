        <u>L'ANIMATION SE MENE EN TROIS TEMPS: </u>       
        <br> <br>
        <b>Passation de l'outil - En sous-groupes de 4 à 5 personnes.</b>        
<br><br>
        Pour les deux premiers temps, vous avez préparé pour chaque sous-groupe un paquet composé des cartes mélangées provenant de trois jeux de cartes "métiers".
        
        Pendant les deux premiers temps, aucune information ne sera donnée permettant d'identifier le sexe de la personne. </p>

        <h2>Premier temps</h2>
<hr>
        <p>Vous expliquez que les cartes reprennent des extraits d'interviews de professionnelles interrogé(es) sur leurs activités et conditions de travail. Vous ne dites pas qu'il y en a trois. Vous invitez les participant(es) de chaque sous-groupe à recomposer les jeux de cartes métiers en réunissant en tas celles qui leur évoquent le même métier. La consigne à laquelle les stagiaires ont à répondre est la suivante:
            
            <ul><li>Combien de personnes ont-elles été interviewées? Quelles professions exercent-elles?</li></ul>
        Au bout de 10 à 15 minutes, vous demandez à chaque sous-groupe les réponses aux questions posées et notez au tableau les métiers ainsi identifiés.
        Les stagiaires vont sûrement évoquer un nombre de métiers et de personnes interviewées important, vous leur annoncez alors qu'il y a seulement trois interviews, donc trois métiers à deviner et trois jeux de cartes à reconstituer. Vous leur laisser 5 minutes pour les retrouver.</p>

        <h2>Second temps</h2>
<hr>
        Il s'agit de faire verbaliser les représentations métiers autour du questionnement suivant:
        <ul>
            <li>Quels métiers ont été reconnus? (les noter au tableau tels qu'ils sont énoncés)</li>
            <li>Tous les participant(es) du sous-groupe sont-ils d'accord?</li>
            <li>En quoi consistent ces métiers? </li>
            <li>Qu'est-ce qui vous permis d eles identifier?</li>
            <li>Y'a-t-il des activités qui vous ont étonné(es)?</li>
            <li>Pourquoi?</li>
            <li>Y a t'il des tâches ou des compétences qui vous semblaient définir le métier et qui n'ont pas été évoquées par la personne interviewée?</li>
            <li>Lesquelles?</li>
            <li>Qu'est-ce que peut faire la différence?</li>
            <li>Qu'est-ce qui vous a le plus marqué dans la description du métier?</li>
            <li>Qu'est-ce qui a rendu la première identification difficile?</li>
            <li>Qu'est-ce qui explique que vous avez initialement énoncé plus de trois métiers ?</li>
        </ul>

        Vous pourrez plus tard dans la séance donner les autres appellations (selon le code Rome) et veiller à les écrire au masculin et au féminin (rappelant si nécessaire l'obligation sociale de le faire et les règles de féminisation des nom de métiers).

        Vous pourrez à ce moment la vérifier avec les stagiaires que les jeux avaient été correctement reconstitué. <br>

        Vous pourrez discuter autour:
        <ul>
            <li>de la diversité des tâches de chacun des métiers. Les tâches accomplies dans l'exercice d'un métier sont beaucoup moins limitées que ce que l'on imagine généralemet.</li>
            <li>des fonctions et activités quelquefois différentes selon le profil de poste et les environnements de travail.</li>
            <li>des modes d'occupation d'un métier ou d'un poste très différents d'un individu à un autre, d'un environnement de travail à un autre.</li>
        </ul>
       <p> Vous demanderez enfin de décrire la personne interviewée.
        Est-ce vous pourriez décrire la personne dont vous venez de découvrir le parcours professionnel ?</p>

        <h2>Troisième temps</h2>
<hr>
        Pour chaque sous-groupe, vous choisissez une biographie parmi celles des trois personnes correspondant aux cartes "métiers" étudiées auparavant, et vous remettez à chacun le jeu de cartes "parcours de vie" mélangé. Vous demandez aux participant(es) de reconstituer la biographie de façon chronologique.
<br>
        Vous allez découvrir le parcours de vie de X. Les cartes sont dans le désordre, reconstituez l'ordre des événements de la biographie professionnelle.
<br>
        La représentation que l'on se fait d'un métier est aussi associée à l'idée qu'on se fait des personnes qui l'exercent.
        Si le sexe de la personne n'est pas évoqué, vous le ferez préciser. Vous pourrez alors amener les participant(es) à réfléchir sur la sexuation des métiers et des compétences. Les parcours biographiques donnent quelques éléments pour comprendre les choix dits "atypiques"<div class="">
        <br>
        Une personne de chaque sous -groupe expliquera au grand groupe la trajectoire de vie professionnelle découverte. S'ensuit l'animation suivante:

        <ul>
        <li>Que pensez-vous de sa trajectoire?</li> 
        <li>Qu'est-ce qui l'a amené(e) à faire ce choix de métier?</li> 
        <li>Pensez-vous qu'elle/il est satisfait(e)</li>  
        <li>Qu'est-ce qui vous permet de le dire?</li> 
        <li>Qu'est-ce qui a facilité le parcours d'insertion ou au contrairel'a rendu plus difficile?</li> 
        </ul>
        Les stagiaires découvrent que la plupart de ces histoires sont chaotiques et souvent marquées par des moments d'échecs scolaires ou professionnels, par des périodes de chômage, ou par des périodes de stages. Au final les interviwé(es) disent avoir surmonté les obstacles et réussi leur insertion professionnelle; ils/elles permettent l'identification et concourent à redynamiser les stagiaires.
<br><br>
        <h6> <u>Pour finir la séquence de travail</u> </h6> Vous demanderez aux stagiaires:

        <ol>
        <li>ce qui les a intéressé(es) et ce qu'ils/elles ont retenu. Vous conduirez cette fin de séquence en amenant les stagiaires à faire un retour sur eux/elles. 
        Qu'est-ce qui dans les métiers évoqués vous intéresse le plus ou vous plait le mieux?</li>
        <li>Ce que l'exercice leur a apporté. 
        Qu'avez-vous retiré cet exercice? Que vous a apporté e temps de travail?</li>
        </ol>
<p></p>
        <h2>Durée de l'animination</h2>
<hr>
        3 à 4 heures avec un groupe de 12 à 15 personnes (groupe mixte ou non mixte).
<br>
        Ce travail peutnaussi être mené en individuel.
        Dans ce cas, c'est l'animateur ou l'animatrice qui questionnera la ou le stagiaire pour qu'elle ou il puisse être confronté(es) à ses représentations.
