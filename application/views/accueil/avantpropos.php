
<div id="titre"> <h1>Des métiers, des vies</h1> </div>
    <div class="corp">
        <h2>Avant propos</h2>
        <hr>
        <aside>
                   <span> Le principe de l'animation pédagogique et de la construction de l'outil repose sur la confrontation des représentations métiers que chacun(e) élabore selon ses expériences, son vécu, ses relations avec un témoignage de professionnel(le) décrivant ses activités quotidiennes et conditions de travail. <br> 
            Pour ce faire nous avons interviewé 15 personnes (10 femmes et 5 hommes) exerçant dans la région Nord pas de Calais. 
            Nous avons choisi notre échantillon de personnes suivant des profils et des trajectoires volontairement variés et diversifiés en terme de niveau scolaire (de VI à III), d'âge, d'origine culturelle, de métiers. Pour travailler l'impact des stéréotypes de sexe sur le processus de choix d'orientation nous avons choisi de donner à voir des parcours d'hommes et/ou de femmes évoluant aussi bien en milieu de travail mixte que non mixte. Pour réaliser un outil accessible à toutes et tous (en formation initiale ou continue), nous avons opté pour le recueil de récits de vie professionnelle. Cette approche nous a permis d'évoquer au cours du même entretien la vie professionnelle et le parcours biographique. <br>
            A partir de la question "Pouvez-vous décrire votre métier ?" nous avons recueilli le descriptif des activités, les conditions de travail, l'évocation de l'environnement, des moments de plaisir et des difficultés rencontrées. <br>
            Une seconde question invitant la personne à expliquer son parcours personnel a permis l'identification et la compréhension des moments de choix et la saisie de opportunités rencontrées. <br>
            Les 15 témoignages ont été retranscrits intégralement puis découpés en séquences présentant les différents aspects du métier et les différentes étapes du parcours de vie. <br>
            Pour chaque personne interviewée, deux jeux de cartes ont été réalisés : <br></span>

            <ul>
                <li>Un autour du parcours biographique</li>
                <li>L'autre autour des activités professionnelles</li>
            </ul>
                        <span> 
                            Permettant de révéler le métier exercé. Chaque carte reprend un extrait discursif de l'interview. <br>
                                A travers les cartes "métiers", les publics visés pourront appréhender la diversité des tâches qui concourent à l'exercice d'un métier et débattre à partir d'une approche compétence, qui permet de désexuer les tâches et les métiers. <br>
                                A travers les cartes "parcours de vie", les publics visés pourront s'identifier et entrevoir une diversité de stratégies d'orientation et d'insertion professionnelle. Sans les banaliser, les biographies donnent en effet à voir combien des faits tels que l’allongement du processus d’insertion, la fréquence de l'alternance du chômage, de la formation et des emplois précaires sont des phénomènes sociaux. <br>
                        </span>
            
        </aside>
        <br> <br>
        <h2>Les objectifs</h2>
        <hr>
        <aside>
            <ul>
                <li>Travailler les représentations spontanées des métiers</li>
                <li>Eveiller la curiosité et le questionnement sur la diversité des activités professionnelles</li>
                <li>Faire prendre conscience de la sexuation des rôles professionnels et de l'impact sur les choix et les positionnements professionnels</li>
                <li>Proposer des nouveaux modèles identificatoires professionnels.</li>
            </ul>
        </aside>
<br> <br>
        <h2>Le public visé</h2>
        <hr>
        <aside>
        Cet outil permet aux acteurs et actrices de l'orientation scolaire et professionnelle et aux professionnel-les de la formation initiale et continue de travailler à l'émergence de projets professionnels avec des bénéficiaires de niveau V à niveau III.
        </aside>
<br> <br>
        <h2>Le support</h2>
        <hr>
        <aside>
        L'outil "Des métiers des vies" comprend outre cette application, un livret, deux séries de jeux de cartes :
            <ul>
                <li>Une première série de 15 jeux de cartes " métiers", chaque carte reprenant un élément discursif du parcours professionnel : la description soit d'une tâche ou d'une activité, soit d'une condition de travail.</li>
                <li>Une seconde série de 15 jeux de cartes "parcours de vie", chaque carte reprenant un élément discursif du parcours biographique des personnes interviewées.</li>
                Chaque personne interviewée donne à voir un métier différent.
            </ul>
        </aside>
